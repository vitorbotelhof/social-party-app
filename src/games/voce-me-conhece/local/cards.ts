function tagsUnicas(tags: TagEditorialVMC[]): TagEditorialVMC[] {
  return [...new Set(tags)];
}

function modoRecomendado(card: CartaoVMCBase): ModoEscolhaRecomendadoVMC {
  if (card.temperatura === 'pessoal') return 'top1';
  return card.tipoEscolha === 'ambos' ? 'ambos' : 'top1';
}

function criarMetadados(card: CartaoVMCBase): MetadadosEditoriaisVMC {
  const categoriaId = card.categoriaId;

  return {
    familia: FAMILIA_POR_CATEGORIA[categoriaId] ?? 'leitura-social',
    tags: tagsUnicas([
      ...(TAGS_BASE_POR_CATEGORIA[categoriaId] ?? ['comportamento']),
      ...(TAGS_POR_PREFIXO[card.id] ?? []),
    ]),
    intensidade: INTENSIDADE_POR_CATEGORIA[categoriaId] ?? 'media',
    riscoRepeticao: RISCO_POR_CATEGORIA[categoriaId] ?? 'medio',
    modoRecomendado: modoRecomendado(card),
  };
}

function enriquecerCard(card: CartaoVMCBase): CartaoVMC {
  return {
    ...card,
    editorial: criarMetadados(card),
  };
}

