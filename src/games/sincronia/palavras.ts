export function montarBaralho(incluirDificil: boolean): string[] {
  const base = [...PALAVRAS_FACIL, ...PALAVRAS_MEDIO];
  const palavras = incluirDificil ? [...base, ...PALAVRAS_DIFICIL] : base;
  // Fisher-Yates shuffle
  const baralho = [...palavras];
  for (let i = baralho.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const tmp = baralho[i]!;
    baralho[i] = baralho[j]!;
    baralho[j] = tmp;
  }
  return baralho;
}
